// Define Tree structure
class Node {
public:
    int data;
    Node* left;
    Node* right;

    // Constructor
    Node(int d) {
        data = d;
        left = right = NULL;
    }
};


//Naive solution O(n)
int countBT(Node *root){
    if(root == NULL){
        return 0;
    }

    return 1 + countBT(root->left) + countBT(root->right);
}


int main(){
    Node *root = new Node(10);
    Node *l1 = new Node(20);
    Node *r1 = new Node(30);
    Node *l2 = new Node(40);
    Node *r2 = new Node(50);
    Node *l3 = new Node(60);

    root->left = l1;
    root->right = r1;
    l1->left = l2;
    l1->right = r2;
    r1->left = l3;

    int ans = countBT(root);
    cout<<ans<<endl;




    return 0;
}