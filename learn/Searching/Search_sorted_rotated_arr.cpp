#include<bits/stdc++.h>

using namespace std;


int Search_Rotated(int arr[] , int n , int x){

    int low = 0 , high = n-1;
    while(high>=low){
        int mid = (high + low)/2;
        if(arr[mid]==x){
            return mid;
        }
        

    }


}


